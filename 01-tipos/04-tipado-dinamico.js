// Javascript es un leguaje de tipado dinamico, ya que podemos cambiar los tipos de valores
// de las variables sin hacer ningun tipo de renombrado.
let numero = 42;
let nombre = "Hola Mundo";
let verdadero = true;
let undef;
let nulo = null;

// con la palabra reservada typeof podemos ver que tipo de valor es
// Ex. number, string, boolean, undefined, object
console.log(typeof numero);
console.log(typeof nombre);
console.log(typeof verdadero);
console.log(typeof undef);
console.log(typeof nulo);

