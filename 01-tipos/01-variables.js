// Para declarar una variable utilizamos la palabra reservada let (existen también la palabras...
// ... reservadas var y const para crear variables).
// Luego de la palabra let se coloca el nombre de la variable y no puede ser utilizada una palabra...
// ... reservada como nombre para las variables.
// Los nombres de las variables deben comenzar en letras, no pueden iniciar en números.
// Luego lleva el signo = para darle valor, en caso de no colocarlo javascript igual la crea con...
// ... el valor undefined y luego le podemos asignar un valor.

let nombre = "Hola Mundo";

console.log(nombre);

